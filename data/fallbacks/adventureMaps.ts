// Format String: '#' = Tembok, '.' = Lantai
// Ukuran: 40 Baris x 60 Kolom

const normalizeMap = (mapData: string[]): string[] => {
    const TARGET_ROWS = 40;
    const TARGET_COLS = 60;
    let normalized = [...mapData];
    // Fill Rows
    if (normalized.length < TARGET_ROWS) {
        const diff = TARGET_ROWS - normalized.length;
        for(let i=0; i<diff; i++) normalized.push("#".repeat(TARGET_COLS));
    } else {
        normalized = normalized.slice(0, TARGET_ROWS);
    }
    // Fill Cols
    return normalized.map(row => {
        if (row.length < TARGET_COLS) return row + "#".repeat(TARGET_COLS - row.length);
        return row.slice(0, TARGET_COLS);
    });
};

